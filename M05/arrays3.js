// Los métodos unshift(), push(), shift() y pop() son métodos integrados en JavaScript que se utilizan para manipular los elementos de un array. Aquí tienes una descripción de cada uno de ellos:
var colores = ["amarillo", "azul"];

colores.unshift("verde"); //lo agrega al principio del arreglo
colores.push("rojo"); //lo agrega al final del arreglo
colores.shift(); //Elimina el elemento que esta al inicio
colores.pop(); //Elimina el elemento final
console.log(colores);

