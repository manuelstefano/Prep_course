//ARRAYS
// En el código proporcionado, se trabaja con un array llamado listaDeCompras. Un array es una estructura de datos que permite almacenar múltiples elementos en una sola variable. Cada elemento dentro del array tiene una posición numérica llamada índice.

// Aquí está el flujo de ejecución del código:

var listaDeCompras = []; // Se declara un array vacío llamado listaDeCompras utilizando la notación de corchetes [].

// Se asignan valores a elementos individuales del array utilizando la notación de corchetes y el índice correspondiente. En este caso, se asignan los siguientes valores:
listaDeCompras[3] = "Tomates";
listaDeCompras[2] = "Papa";
listaDeCompras[1] = "Zanahoria";
listaDeCompras[0] = "Lechuga";
// Los índices empiezan desde 0, por lo que listaDeCompras[0] se refiere al primer elemento, listaDeCompras[1] al segundo elemento, y así sucesivamente.

// console.log(listaDeCompras);

let elementoDelArray = listaDeCompras[1]; // Se declara una variable elementoDelArray y se le asigna el valor de listaDeCompras[1]. Esto significa que se está accediendo al segundo elemento del array y se almacena en la variable.

console.log(elementoDelArray) // En la consola, se mostrará el elemento "Zanahoria", ya que se accede al segundo elemento del array listaDeCompras a través de listaDeCompras[1] y se imprime su valor.